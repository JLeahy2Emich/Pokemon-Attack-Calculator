    <ul style=font-size:20px>
        <li>Attacking Type: The type of the move that is being used by you, against the defending pokemon (is not used in Defending calculations).</li>
        <li>Defending Type 1: The type that the defending pokemon is, the game does not give you this information, so you may need to use a resource, such as Bulbapedia, to find this.</li>
        <li>Defending Type 2: Some Pokemon have 2 types, and both contribute to the effectiveness of moves used against them, so make sure you enter both if the pokemon has 2 types.</li>
    </ul>
